using namespace std;

int n;
map<int,int> m;

int main(){
	sd(n);
	int up = n*n;
	for(int i = 0; i < up; i++){
		int x; sd(x);
		m[x]++;
	}
	
	vector<int> v;
	while(v.size() < n){
		int mx = 0;
		foreach(it, m){
			mx = max(mx, it->fi);
		}
		
		m[mx]--;
		if(m[mx] == 0) m.erase(mx);
		foreach(it, v){
			int g = __gcd(*it, mx);
			m[g] -= 2;
			if(m[g] == 0) m.erase(g);
		}
		v.pb(mx);
	}
	
	foreach(it, v){
		printf("%d ", *it);
	}	
	printf("\n");
		
	return 0;
}
