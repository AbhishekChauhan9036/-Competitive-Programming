struct line{
	long double m, c, x, y;
	bool inf, zero;
	
	void assign(point &p1, point &p2){
		if(p1.fi == p2.fi){
			inf = true;
			x = p1.fi;
		}
		if(p1.se == p2.se){
			zero = true;
			y = p1.se;		
		}
		else{
			inf = zero = false;
			m = (p1.se-p2.se)/(p1.fi-p2.fi);
			c = p1.se - m*p1.fi;
		}
	}
	
};



