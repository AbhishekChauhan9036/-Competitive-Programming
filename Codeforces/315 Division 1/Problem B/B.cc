using namespace std;

long long dp[4101][4101], MOD = 1e9 + 7;

int main(){
	dp[1][1] = 1;
	int last = 1;
	for(int i = 2; i <= 4010; i++){
		dp[i][1] = dp[i-1][last];
		for(int j = 2; j <= 4010; j++){
			dp[i][j] = (dp[i][j-1] + dp[i-1][j-1])%MOD;
		}
		last = i;
	}

	int n;
	cin >> n;
	cout << (dp[2+n][1] - dp[1+n][1] + MOD)%MOD << "\n";
	
	return 0;
}
