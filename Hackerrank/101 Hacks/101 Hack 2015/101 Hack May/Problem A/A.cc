using namespace std;

string s;
int ans = 1, mod = 1000;
int cnt[26];

int main(){	
	cin >> s;
	for(int i = 0; i < s.length(); i++){
		cnt[s[i]-'a']++;
	}
	
	for(int i = 0; i < 26; i++){
		if(cnt[i] > 0) (ans *= cnt[i]) %= mod;
	}
	
	tr(ans);
	
	return 0;
}
